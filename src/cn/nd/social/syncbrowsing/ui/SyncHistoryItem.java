package cn.nd.social.syncbrowsing.ui;


public class SyncHistoryItem {
	
	private String mHistoryName;
	private long mHistoryType;
	private String mHistoryPath;
	private long mHistoryTime;

	public String getmHistoryName() {
		return mHistoryName;
	}
	public void setmHistoryName(String string) {
		this.mHistoryName = string;
	}
	
	public long getmHistoryType() {
		return mHistoryType;
	}
	public void setmHistoryType(long mHistoryType) {
		this.mHistoryType = mHistoryType;
	}
	
	public String getmHistoryPath() {
		return mHistoryPath;
	}
	public void setmHistoryPath(String mHistoryPath) {
		this.mHistoryPath = mHistoryPath;
	}
	
	public long getmHistoryTime() {
		return mHistoryTime;
	}
	public void setmHistoryTime(long mHistoryTime) {
		this.mHistoryTime = mHistoryTime;
	}

	
}
